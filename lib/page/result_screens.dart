  void _shareToSpecificParticipant(Participant participant) {
    final splitResult = billData.calculateSplit();
    final amount = splitResult[participant.id] ?? 0;

    if (amount <= 0) return;

    String message = 'Hi ${participant.name}!\n\n';
    message += 'ðŸ“‹ *YOUR BILL SPLIT DETAIL*\n\n';

    // Show items assigned to this participant
    message += 'ðŸ“ *YOUR ITEMS:*\n';
    for (var item in billData.items) {
      if (item.assignedTo.contains(participant.id)) {
        final totalParticipants = item.assignedTo.length;
        final yourShare = item.totalPrice / totalParticipants;

        message += '\n*${item.name}*\n';
        message += 'Quantity: ${item.quantity}\n';
        message += 'Price/Unit: ${PriceUtils.formatPrice(item.pricePerUnit)}\n';
        message += 'Total: ${PriceUtils.formatPrice(item.totalPrice)}\n';
        message += 'Shared by: $totalParticipants people\n';
        message += 'Your share: ${PriceUtils.formatPrice(yourShare)}\n';
      }
    }

    message += '\nâ”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”\n\n';
    message += 'ðŸ’° *PAYMENT SUMMARY*\n\n';
    message += 'Total Amount to Pay: *${PriceUtils.formatPrice(amount)}*\n\n';

    message += '\n_Generated by Billo App_\n';
    message += '_Please make your payment. Thank you!_';

    final phoneNumber = participant.phoneNumber.startsWith('0')
        ? '62${participant.phoneNumber.substring(1)}'
        : participant.phoneNumber.startsWith('+62')
        ? participant.phoneNumber.substring(1)
        : participant.phoneNumber;

    final encodedMessage = Uri.encodeComponent(message);
    final url =
        'https://api.whatsapp.com/send/?phone=$phoneNumber&text=$encodedMessage';

    _launchUrl(url);
  }

  Future<void> _launchUrl(String url) async {
    if (await canLaunch(url)) {
      await launch(url);
    } else {
      throw 'Could not launch $url';
    }
  }

